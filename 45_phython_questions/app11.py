# This program demonstrates how to use lists and loops to print items (friends' names, fruits, and animals).

# Example 1: Friends' Names
friends_name = ['anna', 'nimra', 'sunny', 'shan']  # List of friends' names

print("\nFriends' Names:")  # Print a heading for clarity
for name in friends_name:  # For loop to print each friend's name
    print(name)

# Example 2: Fruits
fruits = ['apple', 'banana', 'cherry', 'grape']  # List of fruits

print("\nFruit Names:")  # Print a heading for clarity
for fruit in fruits:  # For loop to print each fruit
    print(fruit)

# Example 3: Animals
animals = ['lion', 'tiger', 'elephant', 'giraffe']  # List of animals

print("\nAnimal Names:")  # Print a heading for clarity
for animal in animals:  # For loop to print each animal
    print(animal)