# Subtraction operation: Subtracts one number from another and prints the result
print(10 - 2)  

# Multiplication operation: Multiplies two numbers and prints the result
print(4 * 2)  

# Division operation: Divides one number by another and prints the result (integer division)
# Note: The '//' operator ensures integer division, discarding any remainder.
